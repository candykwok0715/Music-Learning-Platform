       <tr>
          <th scope="row"><p>A tempo</p>
          <p> [ Italian ]</p></th>
          <td><p>In time  , a return to the original speed , especially after ritardando ( rit. ) or  rallentando ( rall ).</p></td>
          <td><p>原速 恢復原先的速度，特別用於rit. &lsquo;rall. 或accel. 等表示速度變化的標記之後。</p></td>         
       </tr>
           
       <tr>
          <th scope="row"><p>Adagio</p>
          <p> [ Italian ]</p></th>
          <td><p>At ease, slow and leisurely; a speed faster  than largo but slower than andante.</p></td>
          <td><p>柔板 ( 或稱慢板 ) 柔和，舒緩而悠閑的。速度較廣板( largo)為快，但慢於行板(andante)。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Allegro</p>
          <p> [ Italian ]</p></th>
          <td><p>Quick, cheerful, lively – indicating a  fairly quick speed.</p></td>
          <td><p>快板 原意為歡樂，活潑，引伸為快速。</p></td>
       </tr>
           
       <tr>
          <th scope="row"><p>Allegretto</p>
          <p>[ Italian ]</p></th>
          <td><p>Broadening, getting slower and louder. </p></td>
          <td><p>小快板 適中的快速，但慢於快板(allegro)。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Andante</p>
          <p> [ Italian ]</p></th>
          <td><p>At a walking pace, at a moderate speed,  moving along.</p></td>
          <td><p>行板 行走的，流動的。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Brio</p>
          <p> [ Italian ]</p></th>
          <td><p>Vigour, spirit.</p></td>
          <td><p>活力，精神，精力。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Cantabile</p>
          <p> [ Italian ]</p></th>
          <td><p>In a melodious, singing style.</p></td>
          <td>如歌唱一般的風格。</td>
       </tr>

       <tr>
          <th scope="row"><p>Co</p>
          <p>l [ Italian ]</p></th>
          <td><p>With the.</p></td>
          <td><p>和，用，因，以。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Con moto</p>
          <p> [ Italian ]</p></th>
          <td><p>With motion, with movement.</p></td>
          <td><p>活躍地，流動地；速度稍快。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Con sordini</p>
          <p> [ Italian ]</p></th>
          <td><p>With mutes. Often abbreviated to con sord.</p></td>
          <td><p>用弱音器。縮寫為con sord.。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Da capo</p>
          <p> [ Italian ]</p></th>
          <td><p>Repeat from the beginning. Often  abbrevianted to D.C.</p></td>
          <td><p>從頭再奏。縮寫為D.C.。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Decrescendo</p>
          <p> [ Italian ]</p></th>
          <td><p>Gradually becoming softer. Often  abbreviated to <strong><em>decres.</em></strong>or<strong> decresc.</strong></p></td>
          <td><p>漸弱 縮寫為decres.或deccres.</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Dolce</p>
          <p> [ Italian ]</p></th>
          <td><p>Gentle, swet, soft.</p></td>
          <td><p>溫柔的，甜蜜的，柔和的。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Ed</p>
          <p> [ Italian ]</p></th>
          <td><p>And.</p></td>
          <td><p>和，與，及。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Espressivo</p>
          <p>   [ Italian ]</p></th>
          <td><p>Expressive. Often abbreviated to <strong><em>espr.</em></strong> or<em> <strong>espress</strong>.</em></p></td>
          <td><p>有表情的。縮寫為espr.或espress.</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Fine</p>
          <p>[ Italian ]</p></th>
          <td><p>End, conclusion. Same as fin.</p></td>
          <td><p>終止，完結，曲終。與fin相同。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Fortepino </p>
          <p> [ Italian ]</p></th>
          <td><p>First loudly, then immediately softly.  Often abbreviated to <img width="24" height="20" src="TeachingMaterial/clip_image002.png" > .</p></td>
          <td><p>開始強，隨即弱。縮寫為 <img width="24" height="20" src="TeachingMaterial/clip_image002.png" > .</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Giocoso</p>
          <p> [ Italian ]</p></th>
          <td><p>Playful, jesting, humorous.</p></td>
          <td><p>嬉戲的，滑稽的，幽默的。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Grazioso </p>
          <p>[ Italian ]</p></th>
          <td><p>Graceful, charming.</p></td>
          <td><p>優美的，優雅的，迷人的。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Legato</p>
          <p>   [ Italian ]</p></th>
          <td><p>In a smooth style, smoothly. (A curved line  over or under two or more different notes, meaning that the notes should be  performed smoothly.)</p></td>
          <td><p>圓滑地，流暢地。(圓滑線，連音，連奏。)</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Lento</p>
          <p> [ Italian ]</p></th>
          <td><p>Slow, at a slow speed.</p></td>
          <td><p>慢板 慢，媛慢。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Ma</p>
          <p> [ Italian ]</p></th>
          <td><p>But.</p></td>
          <td><p>但，但是，然而。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Maestoso</p>
          <p> [ Italian ]</p></th>
          <td><p>Majestic, in a stately, dignified manner.</p></td>
          <td><p>莊嚴的，華貴的，堂皇的。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Mano</p>
          <p> [ Italian ]</p></th>
          <td><p>Hand. Often abbreviated to m.</p></td>
          <td><p>手。縮寫為m.。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Non</p>
          <p> [ Italian ]</p></th>
          <td><p>Not.</p></td>
          <td><p>不，不是，勿。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Pianissimo</p>
          <p> [ Italian ]</p></th>
          <td><p>Very soft, very quiet. Often abbreviated to <strong><em>pp</em></strong> or <strong>pianiss</strong>.</p></td>
          <td>十分弱，十分輕。縮寫為pp或pianiss.。</td>
       </tr>

       <tr>
          <th scope="row"><p>Piano </p>
          <p> [ Italian ]</p></th>
          <td><p>Soft, quiet. Often abbreviated to <strong><em>p</em></strong>.</p></td>
          <td><p>弱，輕。縮寫為p。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Poco </p>
          <p>[ Italian ]</p></th>
          <td><p>A little, slightly.</p></td>
          <td><p>些少，少許，微量的，略微。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Presto </p>
          <p>[ Italian ]</p></th>
          <td><p>Fast, faster than allegro and vivace.</p></td>
          <td><p>急板 比板(allegro)及活板(vivace)更快。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Rallentando</p>
          <p> [ Italian ]</p></th>
          <td><p>Gradually slowing down. Often abbreviated  to <strong>rall.</strong></p></td>
          <td><p>逐漸慢。縮寫為rall.。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Ritenuto </p>
          <p>[ Italian ]</p></th>
          <td><p>Held back, immediately slower. Often  abbreviated to rit. or riten.</p></td>
          <td><p>突慢，突然放慢。縮寫為rit.或riten.。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Senza sordini</p>
          <p> [ Italian ]</p></th>
          <td><p>Without mutes. Often abbreviated to senza  sord.</p></td>
          <td><p>不用弱音器，去除弱音器。縮寫為senza sord.</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Sostenuto</p>
          <p> [ Italian ]</p></th>
          <td><p>Sustained, prolonged. Often abbreviated to <strong>sost.</strong></p></td>
          <td><p>持續的，保持的，飽滿的，綿延的。縮寫為sost.。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Staccatissimo [ Italian ]</p></th>
          <td><p>Very short, as short as possible. </p></td>
          <td><p>極短的跳音，短斷音。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Sul G</p>
          <p> [ Italian ]</p></th>
          <td><p>On the g string</p></td>
          <td><p>在G弦上。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Tre corde</p>
          <p> [ Italian ]</p></th>
          <td><p>Three strings; an instruction to a pianist  to release the soft(left) pedal.</p></td>
          <td><p>三根弦，即放開(鋼琴的)弱音踏板。縮寫為t.c。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Troppo </p>
          <p>[ Italian ]</p></th>
          <td><p>Too much</p></td>
          <td><p>太多：太，過份。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Una corda </p>
          <p>[ Italian ]</p></th>
          <td><p>One string; an instruction to a pianist to  press the soft(left) pedal. Often abbreviated to u.c.</p></td>
          <td><p>一根弦，即用(鋼琴的)弱音踏板。縮寫為u.c.。</p></td>
       </tr>

       <tr>
          <th scope="row"><p>Vivo</p>
          <p> [ Italian ]</p></th>
          <td><p>Lively, alive.</p></td>
          <td><p>活潑，活躍的。</p></td>
       </tr>

